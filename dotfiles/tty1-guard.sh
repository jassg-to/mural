#!/usr/bin/env bash

if [ "$(tty)" != "/dev/tty1" ]; then
    exit 1
fi

startx

cat <<EOF
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    ****************************************************************
    ***                                                          ***
    ***  Waiting 10 seconds before restarting...                 ***
    ***  Press Ctrl+C to enter the system shell.                 ***
    ***                                                          ***
    ****************************************************************
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
EOF
sleep 10
